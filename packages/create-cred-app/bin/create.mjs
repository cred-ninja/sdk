#!/usr/bin/env node

/**
 * create-cred-app — Scaffold a self-hosted Cred server
 *
 * Usage:
 *   npx create-cred-app my-cred-server
 *   npx create-cred-app .              # scaffold in current directory
 */

import { mkdirSync, writeFileSync, copyFileSync, existsSync, readdirSync, statSync } from 'node:fs';
import { join, resolve, dirname, basename } from 'node:path';
import { execSync } from 'node:child_process';
import { fileURLToPath } from 'node:url';
import { randomBytes } from 'node:crypto';

const __filename = fileURLToPath(import.meta.url);
const __dirname = dirname(__filename);
const TEMPLATE_DIR = join(__dirname, '..', 'template');

const name = process.argv[2];

if (!name || name === '--help' || name === '-h') {
  console.log(`
  \x1b[1mcreate-cred-app\x1b[0m — Scaffold a self-hosted Cred server

  \x1b[36mUsage:\x1b[0m
    npx create-cred-app <directory>
    npx create-cred-app my-server
    npx create-cred-app .

  \x1b[1mWhat you get:\x1b[0m
    • Express server powered by @credninja/server
    • SQLite vault (zero-config, no external DB)
    • Admin UI at /connect for managing OAuth providers
    • Pre-configured for Google, GitHub, Slack
    • Docker + Caddy deployment option
  `);
  process.exit(name ? 0 : 1);
}

const targetDir = resolve(process.cwd(), name);
const projectName = name === '.' ? basename(process.cwd()) : name;

console.log();
console.log(`  \x1b[1m🦀 create-cred-app\x1b[0m`);
console.log();

// Create directory
if (name !== '.') {
  if (existsSync(targetDir)) {
    console.error(`  \x1b[31m✗\x1b[0m Directory "${name}" already exists.`);
    process.exit(1);
  }
  mkdirSync(targetDir, { recursive: true });
  console.log(`  \x1b[32m✓\x1b[0m Created ${name}/`);
}

// Copy template files recursively
function copyDir(src, dest) {
  mkdirSync(dest, { recursive: true });
  for (const entry of readdirSync(src)) {
    const srcPath = join(src, entry);
    const destPath = join(dest, entry);
    if (statSync(srcPath).isDirectory()) {
      copyDir(srcPath, destPath);
    } else {
      copyFileSync(srcPath, destPath);
      console.log(`  \x1b[32m✓\x1b[0m ${destPath.replace(targetDir + '/', '')}`);
    }
  }
}

copyDir(TEMPLATE_DIR, targetDir);

// Generate .env with random passphrase and tokens
const passphrase = randomBytes(32).toString('base64url');
const adminToken = 'cred_admin_' + randomBytes(16).toString('hex');
const agentToken = 'cred_at_' + randomBytes(16).toString('hex');

const envContent = `# Generated by create-cred-app
# ⚠️  Keep this file secure — it contains your encryption passphrase

# Vault encryption passphrase (DO NOT LOSE — encrypts all stored tokens)
VAULT_PASSPHRASE=${passphrase}

# Server config
PORT=3456
HOST=127.0.0.1
PUBLIC_URL=http://localhost:3456

# Admin token (for management APIs)
ADMIN_TOKEN=${adminToken}

# Agent token (agents use this to request credentials)
AGENT_TOKEN=${agentToken}

# ─── Provider OAuth Credentials ───────────────────────────
# Uncomment and fill in the providers you want to use.
# Get credentials from each provider's developer console.

# Google: https://console.cloud.google.com/apis/credentials
# GOOGLE_CLIENT_ID=
# GOOGLE_CLIENT_SECRET=
# GOOGLE_DEFAULT_SCOPES=openid,email,profile

# GitHub: https://github.com/settings/developers
# GITHUB_CLIENT_ID=
# GITHUB_CLIENT_SECRET=
# GITHUB_DEFAULT_SCOPES=repo,user

# Slack: https://api.slack.com/apps
# SLACK_CLIENT_ID=
# SLACK_CLIENT_SECRET=
# SLACK_DEFAULT_SCOPES=chat:write,channels:read
`;

writeFileSync(join(targetDir, '.env'), envContent);
console.log(`  \x1b[32m✓\x1b[0m .env (generated with random passphrase + tokens)`);

// Install dependencies
console.log();
console.log(`  \x1b[36mInstalling dependencies...\x1b[0m`);
try {
  execSync('npm install', { cwd: targetDir, stdio: 'pipe' });
  console.log(`  \x1b[32m✓\x1b[0m Dependencies installed`);
} catch (e) {
  console.log(`  \x1b[33m⚠\x1b[0m npm install failed — run it manually`);
}

// Done
console.log();
console.log(`  \x1b[1m\x1b[32m✓ Done!\x1b[0m`);
console.log();
console.log(`  \x1b[1mNext steps:\x1b[0m`);
if (name !== '.') {
  console.log(`  \x1b[36mcd ${name}\x1b[0m`);
}
console.log(`  \x1b[36mnpm start\x1b[0m`);
console.log();
console.log(`  \x1b[1mThen:\x1b[0m`);
console.log(`  1. Open \x1b[36mhttp://localhost:3456/connect\x1b[0m to manage providers`);
console.log(`  2. Add OAuth credentials to \x1b[36m.env\x1b[0m`);
console.log(`  3. Give your agent the token: \x1b[33m${agentToken.slice(0, 12)}...\x1b[0m`);
console.log();
console.log(`  \x1b[90mDocs: https://cred.ninja/docs\x1b[0m`);
console.log();
